public class WeatherInfo {

    private String city;

    private String temp;

    private String time;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getTemp() {

        return temp;
    }

    public void setTemp(String temp) {
        this.temp = temp;
    }

    public String getCity() {

        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
